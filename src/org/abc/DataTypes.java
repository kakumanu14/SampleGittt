package org.abc;

public class DataTypes {
	public static void main(String[] args) {
		//int -> numeric values
		int i = 30;
		System.out.println("int value = "+i);
		
		//char -> single -> alphabet, defined in single quote
		char c = 'a';
		System.out.println("Character value = "+c);
		
		//long data type - append the long value with l
		long lo = 123456789l;
		System.out.println("Long value = "+lo);
		
		//String data type - placed inside the double quotes
		String s = "testing123!@#";
		System.out.println("String value = "+s);
		
		//float data type - decimal values, append with f
		float f = 25.5f;
		System.out.println("Float value = "+f);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
